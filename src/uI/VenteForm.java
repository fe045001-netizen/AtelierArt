    public void loadClient() {
    clients.removeAllItems(); 
    for (Client c : cs.findAll()) {
        clients.addItem(c);  
    }
    }
     public void loadOeuvre() {
      oeuvres.removeAllItems(); 
       for (Oeuvre o : os.findAll()) {
        oeuvres.addItem(o);  
    }
}

    /**
     *
     */
    public void loadVenteArt(){
         model.setRowCount(0);
         for(VenteArt v :vs.findAll() ){
             model.addRow(new Object []{
                 v.getId(),
                 v.getOeuvre(),
                 v.getClient(),
                 v.getDateVente()
             });
         }
     }

