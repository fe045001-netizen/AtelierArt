    private void deleteBnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBnActionPerformed
        // TODO add your handling code here:

        // TODO add your handling code here:

        int reponse =JOptionPane.showConfirmDialog(this, "Voulez vous vraiment annuler ce vent ? " );
        if(reponse ==0){
            vs.delete(vs.findById(id));
            loadVenteArt();
            vider();
        }
    }//GEN-LAST:event_deleteBnActionPerformed

    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
       int reponse = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier cette vente ?");

    if (reponse == JOptionPane.YES_OPTION) {

        Oeuvre oeuvre = (Oeuvre) oeuvres.getSelectedItem();
        Client client = (Client) clients.getSelectedItem();
        Date datevente = dateVente.getDate();

        VenteArt v = new VenteArt(id, oeuvre, client, datevente);

        vs.update(v);
        loadVenteArt();
        vider();
    }
    }//GEN-LAST:event_modifierActionPerformed

    private void ventesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ventesMouseClicked
        // TODO add your handling code here:
        int selectedRow = ventes.getSelectedRow();
    if (selectedRow >= 0) {

        // ID
        id = (int) ventes.getValueAt(selectedRow, 0);

        // OEUVRE
        Oeuvre selectedOeuvre = (Oeuvre) ventes.getValueAt(selectedRow, 1);
        oeuvres.setSelectedItem(selectedOeuvre);

        // CLIENT
        Client selectedClient = (Client) ventes.getValueAt(selectedRow, 2);
        clients.setSelectedItem(selectedClient);

        // DATE
        try {
            Object dateObj = ventes.getValueAt(selectedRow, 3);

            if (dateObj instanceof Date) {
                dateVente.setDate((Date) dateObj);
            } else {
                String dateStr = dateObj.toString();
                java.util.Date date = new java.text.SimpleDateFormat("yyyy-MM-dd").parse(dateStr);
                dateVente.setDate(date);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_ventesMouseClicked


