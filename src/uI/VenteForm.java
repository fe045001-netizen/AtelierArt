    private void vider() {
          if (oeuvres.getItemCount() > 0) {
        oeuvres.setSelectedIndex(0);
    } else {
        oeuvres.setSelectedItem(null);
    }

    if (clients.getItemCount() > 0) {
        clients.setSelectedIndex(0);
    } else {
        clients.setSelectedItem(null);
    }
    dateVente.setDate(null);
}

