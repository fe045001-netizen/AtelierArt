    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String categorie = (String) categorieItem.getSelectedItem();
        if (categorie == null || categorie.equals("-- Sélectionnez --")) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une catégorie.", "Erreur", JOptionPane.WARNING_MESSAGE);
            return;
        }

        List<Oeuvre> resultats = oc.findByCategorie(categorie);
        model.setRowCount(0);

        if (resultats.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Aucune oeuvre trouvée pour : " + categorie);
        } else {
            for (Oeuvre o : resultats) {
                model.addRow(new Object[]{
                    o.getId(),
                    o.getTitre(),
                    o.getCategorie(),
                    o.getArtiste(),
                    o.getPrix()
                });
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

