      private void loadAllOeuvres() {
        model.setRowCount(0);
        List<Oeuvre> oeuvres = oc.findAll();
        for (Oeuvre o : oeuvres) {
            model.addRow(new Object[]{
                o.getId(),
                o.getTitre(),
                o.getCategorie(),
                o.getArtiste(),
                o.getPrix()
            });
        }
    }
      private void loadCategories() {
    List<String> categories = oc.findCategories(); 
    DefaultComboBoxModel<String> comboModel = new DefaultComboBoxModel<>();
    comboModel.addElement("-- Sélectionnez --");
    for (String cat : categories) {
        comboModel.addElement(cat);
    }
    categorieItem.setModel(comboModel); 
}

